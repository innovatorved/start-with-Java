public class j17EscapeCharacters {
    public static void main(String[] args){
        System.out.println("____Java Escape Characters____");

        // how to print double quotes inside the double quotes
        System.out.println("this is double quotes \"\" ");
        System.out.println("Double quotes : \"a\"b\"z\"c\"d\"e\"f\"");

        // print backslash /
        System.out.println("Print Back Slash : \\");

        //for new line
        System.out.println("for \n \\n new line");

        // fro tab
        System.out.println("for \t \\t tab");
    }
}
