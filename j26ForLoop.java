public class j26ForLoop {
    public static void main(String[] args){
        System.out.println("__For Loop in Java__");

        System.out.println("Print 1 to 10 using for loop");
        for(int i = 0; i <= 10; i++){
            System.out.println(i);
        }

        System.out.println("\nPrint 10 to 1 using for loop");
        for(int i = 10; i >= 1; i--){
            System.out.println(i);
        }
    }
}
